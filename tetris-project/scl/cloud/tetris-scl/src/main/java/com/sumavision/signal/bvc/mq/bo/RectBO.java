package com.sumavision.signal.bvc.mq.bo;

public class RectBO {

	private Long z_index;
	
	private Long width;
	
	private Long height;
	
	private Long x;
	
	private Long y;
	
	private String rect_id;
	
	private String channel;
	
	private String type;

	public Long getZ_index() {
		return z_index;
	}

	public void setZ_index(Long z_index) {
		this.z_index = z_index;
	}

	public Long getWidth() {
		return width;
	}

	public void setWidth(Long width) {
		this.width = width;
	}

	public Long getHeight() {
		return height;
	}

	public void setHeight(Long height) {
		this.height = height;
	}

	public Long getX() {
		return x;
	}

	public void setX(Long x) {
		this.x = x;
	}

	public Long getY() {
		return y;
	}

	public void setY(Long y) {
		this.y = y;
	}

	public String getRect_id() {
		return rect_id;
	}

	public void setRect_id(String rect_id) {
		this.rect_id = rect_id;
	}

	public String getChannel() {
		return channel;
	}

	public void setChannel(String channel) {
		this.channel = channel;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}
