package com.sumavision.tetris.business.yjgb.vo;

public class FileVO {

	/** 文件地址 */
	private String url;
	
	/** 文件播放次数 */
	private Integer count;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}
	
}
